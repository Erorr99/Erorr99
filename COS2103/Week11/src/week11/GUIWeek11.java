    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DNode NEW = new DNode();
        NEW.ID = Integer.parseInt(jTextField1.getText());
        NEW.NAME = jTextField2.getText();
        NEW.SURNAME = jTextField3.getText();
        NEW.SALARY = Float.parseFloat(jTextField4.getText());

        if ((START == null) && (LAST == null)) {
            START = NEW;
            LAST = NEW;
            CUR = NEW;
        } else {
            NEW.FORW = START;
            START.BACK = NEW;
            START = NEW;
            CUR = NEW;
        }
        ForwardTraversingList();
    }//GEN-LAST:event_jButton1ActionPerformed

