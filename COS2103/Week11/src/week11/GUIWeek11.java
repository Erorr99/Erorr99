    public void ForwardTraversingList() {
        jTextArea1.setText("");
        DNode PTR = null;
        PTR = START;
        while (PTR != null) {
            System.out.println(PTR.ID + ':' + PTR.NAME + ':' + PTR.SURNAME + ':' + PTR.SALARY);  //Apply PROCESS to INFOR[PTR]
            jTextArea1.setText(jTextArea1.getText() + "\n" + PTR.ID + ":" + PTR.NAME + ":" + PTR.SURNAME + ":" + PTR.SALARY);
            PTR = PTR.FORW;
        }
    }

    public void BackwardTraversingList() {
        jTextArea1.setText("");
        DNode PTR = null;
        PTR = LAST;
        while (PTR != null) {
            System.out.println(PTR.ID + ':' + PTR.NAME + ':' + PTR.SURNAME + ':' + PTR.SALARY);  //Apply PROCESS to INFOR[PTR]
            jTextArea1.setText(jTextArea1.getText() + "\n" + PTR.ID + ":" + PTR.NAME + ":" + PTR.SURNAME + ":" + PTR.SALARY);
            PTR = PTR.BACK;
        }
    }

    public DNode SEARCH(int ITEM) {
        //Algorrithm 5.2 SEARCH        
        DNode LOC = null;
        DNode PTR = null;
        PTR = START;
        while (PTR != null) {
            if (ITEM == (int) PTR.ID) {
                LOC = PTR;
                return LOC;
            } else {
                PTR = PTR.FORW;
            }
        }
        //Show Result;
        if (LOC == null) {
            System.out.println("Not found.");
        } else {
            System.out.println("Found.");
        }
        return LOC;
    }

    public static void DELTWL(DNode LOC) {
        LOC.BACK.FORW = LOC.FORW;
        LOC.FORW.BACK = LOC.BACK;
    }

    public void INSTWL(DNode LOCA, DNode LOCB, DNode NEW) {
        LOCA.FORW = NEW;
        NEW.FORW = LOCB;
        LOCB.BACK = NEW;
        NEW.BACK = LOCA;
    }

