    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tel = this.TelField.getText();
        String owner = this.OwnerField.getText();
        String number = this.NumberField.getText();
        String type = this.CarTypeDropDown.getSelectedItem().toString();
        if (owner.isEmpty()) {
            JOptionPane.showMessageDialog(null, "You have not entered: Owner ");
            return;
        }
        if (number.isEmpty()) {
            JOptionPane.showMessageDialog(null, "You have not entered: Number");
            return;
        }
        Car car = new Car(number, type, owner, tel, 0);
        CarQueue cq = new CarQueue(++curId, car);
        this.Tab.setSelectedIndex(2);
        this.TelField.setText("");
        this.OwnerField.setText("");
        this.NumberField.setText("");
        String msg = "You get that queue\n\n" + "          " + cq.id + "\n\n" + "Before queue: " + this.queue.count + "\nDatetime: " + cq.dt();
        int status = JOptionPane.showConfirmDialog(null, msg, "Confirm Queue", JOptionPane.YES_NO_OPTION);
        if (status != 0) {
            JOptionPane.showMessageDialog(null, "The queue was successfully canceled.");
            return;
        }
        queue.QINSERT(cq);
        this.hasQueue = true;
        this.display();
        this.jTextField5.setText(car.owner);
        this.jTextField2.setText(car.number);
        this.jTextField3.setText(car.type);
    }//GEN-LAST:event_jButton1ActionPerformed

