    private void SharpButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SharpButtonActionPerformed

        if (this.queue.isFull()) {
            JOptionPane.showMessageDialog(null, "The queue is full.");
            return;
        }
        String tel = this.TelField.getText();

        if (tel.isEmpty()) {
            JOptionPane.showMessageDialog(null, "You haven't entered a phone number.");
            return;
        }
        if (!tel.startsWith("0")) {
            JOptionPane.showMessageDialog(null, "Invalid phone number.");
            return;
        }

        if (tel.length() < 10) {
            JOptionPane.showMessageDialog(null, "You still don't have a 10-digit phone number.");
            return;
        }

        this.Tab.setSelectedIndex(1);
    }//GEN-LAST:event_SharpButtonActionPerformed

