    private void initData() {
        Car[] cars = {
            new Car("กก-123", "6 Wheel", "เอ", "0123456789", 0),
            new Car("ขข-456", "10 Wheel", "บี", "0876543210", 0),
            new Car("คค-789", "Trailer", "ซี", "0123456789", 0),
            new Car("งง-012", "6 Wheel", "ดี", "0876543210", 0),};
        curId = 4;
        for (int i = 0; i < cars.length; i++) {
            CarQueue cq = new CarQueue(i + 1, cars[i]);
            queue.QINSERT(cq);
        }
        int m = 200;
        for (int i = cars.length - 1; i >= 0; i--) {
            queue.QUEUE[i].createAt = LocalDateTime.now().minusMinutes(m);
            m += 20;
        }
    }

    private void addRow(DefaultTableModel model, CarQueue cq) {
        model.addRow(new Object[]{cq.id, cq.dt(), cq.car.owner, cq.car.number, cq.car.type});
    }

    private void display() {
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        model.setRowCount(0);
        if (queue.isEmpty()) {
            return;
        }
        if (queue.FRONT <= queue.REAR) {
            for (int i = queue.FRONT; i <= queue.REAR; i++) {
                addRow(model, queue.QUEUE[i]);
            }
        } else {
            for (int i = queue.FRONT; i <= queue.N; i++) {
                addRow(model, queue.QUEUE[i]);
            }
            for (int i = 0; i <= queue.REAR; i++) {
                addRow(model, queue.QUEUE[i]);
            }
        }
    }

    private void addTel(String value) {
        String tel = this.TelField.getText();
        if (tel.length() >= 10) {
            return;
        }
        this.TelField.setText(tel + value);
    }

    private void removeTel() {
        String tel = this.TelField.getText();
        this.TelField.setText(tel.substring(0, tel.length() - 1));
    }

