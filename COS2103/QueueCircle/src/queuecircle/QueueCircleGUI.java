    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (this.queue.count - 1 != 0) {
            JOptionPane.showMessageDialog(null, "It's not your queue yet.");
            return;
        }
        String type = this.jTextField3.getText();
        String weightStr = this.jTextField4.getText();

        if (weightStr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "You haven't entered your weight yet.");
            return;
        }
        float weight = Float.parseFloat(weightStr);
        if (type.startsWith("6")) {
            if (weight > 10) {
                JOptionPane.showMessageDialog(null, "Weight over 10 tons. will be fined 5,000 baht.");
            }
        } else if (type.startsWith("10")) {
            if (weight > 25) {
                JOptionPane.showMessageDialog(null, "Weight over 25 tons. will be fined 10,000 baht");
            }
        } else if (type.startsWith("T")) {
            if (weight > 50.5) {
                JOptionPane.showMessageDialog(null, "Weight over 50.5 tons. will be fined 30,000 baht");
            }
        }
        JOptionPane.showMessageDialog(null, "Weighed successfully.");
        this.queue.QDELETE();
        this.hasQueue = false;
        this.jTextField5.setText("");
        this.jTextField2.setText("");
        this.jTextField3.setText("");
        this.jTextField4.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

